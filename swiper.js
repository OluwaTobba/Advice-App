// Initialize Swiper
var swiper = new Swiper('.swiper-container', {
    // Set the direction of the swiper (horizontal)
    direction: 'horizontal',
    // Enable touch events
    touchEventsTarget: 'container',
    // Enable mousewheel control
    mousewheel: true,
    // Enable keyboard control
    keyboard: true,
    // Enable grab cursor
    grabCursor: true,
    // Enable pagination
    pagination: {
      el: '.swiper-pagination',
      clickable: true,
    },
  });
  